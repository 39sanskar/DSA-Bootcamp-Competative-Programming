#include <iostream>
using namespace std;

int main() {
  // Creating Infinite loop using while loop
  while (1) {
  }

  // Creating Infinite loop using for loop
  for (;;) {
  }

  // Creating Infinite loop using for loop
  for (int i = 1;; ++i) {
  }
  /*
   You dont put any condition or put true as a condition
  */

  for (int i = 1; true; ++i) {
  }
}
